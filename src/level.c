int level_get_total(void)
{
    return TOTAL_LEVELS;
}

static void parse_row(GameState *gs, const char *row, int r)
{
    int c = 0;
    for (int i = 0; row[i] != '\0'; i++, c++) {
        Tile t = TILE_EMPTY;
        switch (row[i]) {
            case '#': t = TILE_WALL;   break;
            case ' ': t = TILE_FLOOR;  break;
            case '.': t = TILE_TARGET; break;
            case '$':
                t = TILE_FLOOR;
                gs->boxes[gs->box_count].x = c;
                gs->boxes[gs->box_count].y = r;
                gs->box_count++;
                break;
            case '*':
                t = TILE_TARGET;
                gs->boxes[gs->box_count].x = c;
                gs->boxes[gs->box_count].y = r;
                gs->box_count++;
                break;
            case '@':
                t = TILE_FLOOR;
                gs->player.x = c;
                gs->player.y = r;
                break;
            case '+':
                t = TILE_TARGET;
                gs->player.x = c;
                gs->player.y = r;
                break;
            default: t = TILE_EMPTY; break;
        }
        gs->grid[r][c] = t;
        if (c + 1 > gs->cols) gs->cols = c + 1;
    }
}

void level_apply(int index, GameState *gs)
{
    if (index < 0 || index >= TOTAL_LEVELS) return;

    const LevelDef *lv = &levels[index];

    memset(gs->grid, 0, sizeof(gs->grid));
    gs->box_count   = 0;
    gs->move_count  = 0;
    gs->completed   = 0;
    gs->cols        = 0;
    gs->rows        = lv->row_count;
    gs->level_index = index;

    for (int r = 0; r < lv->row_count; r++)
        parse_row(gs, lv->rows[r], r);
}
